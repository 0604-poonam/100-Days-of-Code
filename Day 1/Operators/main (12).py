
x = 25
y = 10

#addition
# Output: x + y = 35
print('x + y =',x+y)

#subtraction
# Output: x - y = 15
print('x - y =',x-y)

#multiplication
# Output: x * y = 250
print('x * y =',x*y)

#division
# Output: x / y = 2.5
print('x / y =',x/y)

#floor division
# Output: x // y = 2
print('x // y =',x//y)

#exponent
# Output: x ** y = 95367431640625
print('x ** y =',x**y)





